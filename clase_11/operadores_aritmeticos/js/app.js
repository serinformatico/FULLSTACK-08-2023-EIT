let a = 10;
let b = 3;
let resultado;

// Suma
resultado = a + b;
console.log("Suma", resultado);

// Resta
resultado = a - b;
console.log("Resta", resultado);

// Producto
resultado = a * b;
console.log("Producto", resultado);

// División
resultado = a / b;
console.log("División", resultado);

// Modulo o resto
resultado = a % b;
console.log("Resto", resultado);