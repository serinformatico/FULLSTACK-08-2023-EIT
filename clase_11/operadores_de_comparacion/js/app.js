let numA = 10;
let numB = 3;
let resultadoDeLaComparacion;

// numA es menor que numB
resultadoDeLaComparacion = numA < numB;
console.log("numA es menor que numB: ", resultadoDeLaComparacion);

// numA es menor o igual que numB
resultadoDeLaComparacion = numA <= numB;
console.log("numA es menor o igual que numB: ", resultadoDeLaComparacion);

// numA es mayor que numB
resultadoDeLaComparacion = numA > numB;
console.log("numA es mayor que numB: ", resultadoDeLaComparacion);

// numA es mayor o igual que numB
resultadoDeLaComparacion = numA >= numB;
console.log("numA es mayor o igual que numB: ", resultadoDeLaComparacion);

// numA es igual a numB
resultadoDeLaComparacion = numA == numB;
console.log("numA es igual a numB: ", resultadoDeLaComparacion);

// numA es diferente a numB
resultadoDeLaComparacion = numA != numB;
console.log("numA es diferente a numB: ", resultadoDeLaComparacion);