const sumar = (numeroA, numeroB) => {
    return numeroA + numeroB;
};

const restar = (numeroA, numeroB) => {
    return numeroA - numeroB;
};

const multiplicar = (numeroA, numeroB) => {
    return numeroA * numeroB;
};

const dividir = (numeroA, numeroB) => {
    return numeroA / numeroB;
};

export { sumar, restar, multiplicar, dividir};